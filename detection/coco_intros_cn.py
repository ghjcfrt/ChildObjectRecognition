def get_intro_by_id(cls_id: int) -> str:
    """按 COCO 类别 ID 获取中文介绍，未命中返回空字符串"""
    return coco_intros_cn.get(int(cls_id), "")


def get_intro_by_label_cn(label_cn: str) -> str:
    """按中文标签获取介绍（将标签反查为 COCO ID 后再取介绍）"""
    inv = {v: k for k, v in coco_labels_cn.items()}
    cls_id = inv.get(label_cn)
    if cls_id is None:
        return ""
    return get_intro_by_id(cls_id)


